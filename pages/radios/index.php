                <section id="features">
                    <div class="row">
                      <div class="columns">
                        <h3>Features</h3>
                        <hr>
                        <p>With an intuitive interface and a responsive design, Gear Player offers an immersive experience to your users when it comes to music playback. You can see it literally come to live and interact with it in several ways.</p>
                      </div>
                    </div>

                    <br><br>

                    <div class="row">

                      <div class="large4 medium-4 columns">
                        <h5>Supported Media</h5>
                        <hr>
                        <p>Gear Player is built on top of the popular SoundManager2 which allows you to feed MP3 files without worrying about crossbrowser issues. It also supports the SoundCloud API natively.</p>
                        <p>When the Flash Plugin is available you are also allowed to use MP4, M4A and AAC files.</p>
                      </div>


                      <div class="large4 medium-4 columns">
                        <h5>Initialization</h5>
                        <hr>
                        <p>Built as jQuery Plugin you can handle it with ease. Just launch it once and it will run for the whole page.</p>
                          <p><pre><code class="language-javascript">$(".gearWrap").gearPlayer();</code></pre></p>
                        <p>You can handle albums through JSON files which will be dynamically loaded to any DOM object with the <i>data-gearPlaylist</i> attribute.</p>
                      </div>



                      <div class="large-4 medium-4 columns">
                          <h5>Controls</h5>
                          <hr>
                          <p>Gear Player supports keyboard shortcuts. Call the next song with <kbd><i class="fi-arrow-up"></i></kbd> <kbd>→</kbd> or the previous one with <kbd><i class="fi-arrow-up"></i></kbd> <kbd>←</kbd>. Press <kbd><i class="fi-arrow-up"></i></kbd> <kbd>↑</kbd> while open to increase volume, and <kbd><i class="fi-arrow-up"></i></kbd> <kbd>↓</kbd> to decrease it.</p>
                          <p>You can do the same with your mousewheel. Move your cursor over the console and roll to adjust the volume. The player is context sensitive!</p>

                      </div>

                    </div>

                    <br><br>

                    <div class="row">

                      <div class="large4 medium-4 columns">
                        <h5>Works with AJAX</h5>
                        <hr>
                        <p>Are you building a website that requires Ajax calls? No problem! Gear Player supports a refresh method that scans new albums and adds them to its routine.</p>
                        <p>When you're done and want to get rid of the player there is also a public destroy method to free up resources and have a proper Garbage Collection.</p>
                      </div>


                      <div class="large4 medium-4 columns">
                        <h5>Mobile Friendly</h5>
                        <hr>
                        <p>Small screens will benefit from a special treatment and allow the player to slide when the playlist button is touched. This way you can scroll through the playlist with just a finger. Pretty much intuitive like a native app!</p>
                        <p>The circular gear is animated with the <a href="http://www.greensock.com/draggable/" target="_blank">TweenMax Draggable</a> component.</p>
                      </div>



                      <div class="large-4 medium-4 columns">
                        <h5>Customizable</h5>
                        <hr>
                        <p>Gear Player can be customized in several ways. It comes with optimized default values, but you're free to play with over 40 parameters. Skin the player by overwriting the existing stylesheet with a custom one.</p>
                        <p>Integrating it inside your own projects will be a piece of cake.</p>
                      </div>

                    </div>

                    <br><br>

                    <div class="row">

                      <div class="large4 medium-4 columns">
                        <h5>SVG Animations</h5>
                        <hr>
                        <p>The player uses <a href="http://raphaeljs.com/" target="_blank">Raphael.js</a> as rendering library and provides you smooth animations driven by <a href="http://www.greensock.com/" target="_blank">Greensock</a>.</p>
                        <p>No matter what resolution and what kind of display you're on, SVG renders well on any device. No more jagged edges!</p>
                      </div>


                      <div class="large4 medium-4 columns">
                        <h5>Canvas Equalizer</h5>
                        <hr>
                        <p>When Flash Player is made available you'll be enabled to view a wonderful, animated, circular equalizer that displays real waves from your music.</p>
                        <p>You can also enable peak values to animated the selected sector and get a complete immersive experience.</p>
                      </div>



                      <div class="large-4 medium-4 columns">
                        <h5>Innovative</h5>
                        <hr>
                        <p>Gear Player pushes HTML5 to its limits by using SVG, Canvas and even Flash to provide a complete new way of listening to your sounds.</p>
                        <p>Despite this it provides an intuitive user interface and engages for its simplicity.</p>
                      </div>

                    </div>

